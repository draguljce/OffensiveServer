package offensive.Server.Hybernate.POJO;

public class Phase {
	private int id;
	private String name;
	
	public Phase() {};
	
	public int getId() {
		return this.id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public String getName() {
		return this.name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
}
